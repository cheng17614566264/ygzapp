package com.cjit.gjsz.interfacemanager.model;

public class SwitchCode{

	private String globusCode;
	private String bopCode;
	private String type;

	public String getGlobusCode(){
		return globusCode;
	}

	public void setGlobusCode(String globusCode){
		this.globusCode = globusCode;
	}

	public String getBopCode(){
		return bopCode;
	}

	public void setBopCode(String bopCode){
		this.bopCode = bopCode;
	}

	public String getType(){
		return type;
	}

	public void setType(String type){
		this.type = type;
	}
}
