package com.cjit.vms.trans.model;

public class SpecialTicket {
	
	private String billId;
	private String billNo;
	private String billCode;
	private String fapiaoType;
	private String buySellInd;
	private String level1Option;
	private String level2Option;
	public String getBillId() {
		return billId;
	}
	public void setBillId(String billId) {
		this.billId = billId;
	}
	public String getBillNo() {
		return billNo;
	}
	public void setBillNo(String billNo) {
		this.billNo = billNo;
	}
	public String getBillCode() {
		return billCode;
	}
	public void setBillCode(String billCode) {
		this.billCode = billCode;
	}
	public String getFapiaoType() {
		return fapiaoType;
	}
	public void setFapiaoType(String fapiaoType) {
		this.fapiaoType = fapiaoType;
	}
	public String getBuySellInd() {
		return buySellInd;
	}
	public void setBuySellInd(String buySellInd) {
		this.buySellInd = buySellInd;
	}
	public String getLevel1Option() {
		return level1Option;
	}
	public void setLevel1Option(String level1Option) {
		this.level1Option = level1Option;
	}
	public String getLevel2Option() {
		return level2Option;
	}
	public void setLevel2Option(String level2Option) {
		this.level2Option = level2Option;
	}
	
	
}
